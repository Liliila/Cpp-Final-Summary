#include<stdio.h>
int main()
{
    char c1, c2, c3, c4;//定义字符型变量c1,c2,c3,c4
    
    c1 = 'A';//给字符型变量c1赋值为ASCII码‘A’
    c2 = 65;//给字符型变量c2赋值为十进制‘65’
    c3 = '\101';//给字符型变量c3赋值为八进制‘101’
    c4 = '\x41';//给字符型变量c4赋值为十六进制‘x41’

    printf("%c,%c,%c,%c\n", c1, c2, c3, c4);//输出字符型c1c2c3c4
    printf("%d,%d,%d,%d\n", c1, c2, c3, c4);//输出十进制整数c1c2c3c4

    getchar();//将控制台停留 等待输入
}//LHF   截图修改后的代码 运行结果